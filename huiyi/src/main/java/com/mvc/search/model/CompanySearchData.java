package com.mvc.search.model;

import java.io.Serializable;
import java.util.List;

public class CompanySearchData implements Serializable{
    private static final long serialVersionUID = -7846962586043678318L;
    private List<CompanyDocument> companys;
    private List<FacetResult> companyAttributeFacets;
    private List<FacetResult> leftSideAttributeFacets;
    public List<CompanyDocument> getCompanys() {
        return companys;
    }
    public void setCompanys(List<CompanyDocument> companys) {
        this.companys = companys;
    }
    public List<FacetResult> getCompanyAttributeFacets() {
        return companyAttributeFacets;
    }
    public void setCompanyAttributeFacets(List<FacetResult> companyAttributeFacets) {
        this.companyAttributeFacets = companyAttributeFacets;
    }
    public List<FacetResult> getLeftSideAttributeFacets() {
        return leftSideAttributeFacets;
    }
    public void setLeftSideAttributeFacets(List<FacetResult> leftSideAttributeFacets) {
        this.leftSideAttributeFacets = leftSideAttributeFacets;
    }

}
