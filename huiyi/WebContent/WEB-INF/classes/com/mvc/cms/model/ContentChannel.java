package com.mvc.cms.model;

import java.util.Date;
import javax.persistence.Entity;
import com.mvc.framework.model.BaseEntity;

/**
 * 文章分类
 */
@Entity(name = "T_CONTENT_CHANNEL")
public class ContentChannel extends BaseEntity {

	private static final long serialVersionUID = 1L;

	// 优先级
	private Integer priority;
	// 栏目名称
	private String channelName;
	// 栏目英文名称
	private String channelNameEn;
	// 栏目级别
	private Integer channelLevel;
	// 上级栏目id
	private Long parentId;

	/**
	 * 系统记录
	 */
	// 状态
	private Integer status;
	// 是否删除
	private Integer isDeleted;
	// 创建时间
	private Date createdDate;
	// 创建人
	private String createdBy;
	// 修改时间
	private Date updatedDate;
	// 修改人
	private String updatedBy;
	
	public Integer getPriority() {
		return priority;
	}
	public void setPriority(Integer priority) {
		this.priority = priority;
	}
	public String getChannelName() {
		return channelName;
	}
	public void setChannelName(String channelName) {
		this.channelName = channelName;
	}
	public String getChannelNameEn() {
		return channelNameEn;
	}
	public void setChannelNameEn(String channelNameEn) {
		this.channelNameEn = channelNameEn;
	}
	public Integer getChannelLevel() {
		return channelLevel;
	}
	public void setChannelLevel(Integer channelLevel) {
		this.channelLevel = channelLevel;
	}
	public Long getParentId() {
		return parentId;
	}
	public void setParentId(Long parentId) {
		this.parentId = parentId;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	public Integer getIsDeleted() {
		return isDeleted;
	}
	public void setIsDeleted(Integer isDeleted) {
		this.isDeleted = isDeleted;
	}
	public Date getCreatedDate() {
		return createdDate;
	}
	public void setCreatedDate(Date createdDate) {
		this.createdDate = createdDate;
	}
	public String getCreatedBy() {
		return createdBy;
	}
	public void setCreatedBy(String createdBy) {
		this.createdBy = createdBy;
	}
	public Date getUpdatedDate() {
		return updatedDate;
	}
	public void setUpdatedDate(Date updatedDate) {
		this.updatedDate = updatedDate;
	}
	public String getUpdatedBy() {
		return updatedBy;
	}
	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}
}